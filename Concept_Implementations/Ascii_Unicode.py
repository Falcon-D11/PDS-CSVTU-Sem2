#Ascii and Unicode values of a character
# Ascii values of a character
print(ord('a'))
print(ord('A'))
# Unicode values of a character
print(ord('€'))
print(ord('¥'))
# Character of an Ascii value
print(chr(97))
print(chr(65))
# Character of a Unicode value
print(chr(8364))
print(chr(165))
# Output:
# 97
# 65
# 8364
# 165
# a
# A
# €
# ¥
