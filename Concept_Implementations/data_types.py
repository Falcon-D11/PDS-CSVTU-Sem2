#Numeric Data Types
#int
int = 100
print("This is ",type(int))
#float
float = 100.0
print("This is ",type(float))
#complex
complex = 1+2j
print("This is ",type(complex))
#Sequence Data Types
#String
string = "Hello"
print("This is ",type(string))
#List
list = [1,2,3,4,5]
print("This is ",type(list))
#Tuple
tuple = (1,2,3,4,5)
print("This is ",type(tuple))
#Set
set = {1,2,3,4,5}
print("This is ",type(set))
#Dictionary
dict = {"name":"John", "age":25}
print("This is ",type(dict))
#Boolean Data Type
boolean = True
print("This is ",type(boolean))

