#Typecasting in python
#Typecasting is the process of converting one data type to another data type.
#There are two types of typecasting:
#Implicit Typecasting
#Explicit Typecasting
#Implicit Typecasting
#Implicit typecasting is done automatically by the compiler.
#In this type of typecasting, the compiler automatically converts one data type to another data type.
#Implicit typecasting is also known as automatic typecasting.
#Implicit typecasting is done when:
#A smaller data type is converted to a larger data type.
#For example, converting an integer to a float.
#Explicit Typecasting
#Explicit typecasting is done manually by the programmer.
#In this type of typecasting, the programmer manually converts one data type to another data type.
#Explicit typecasting is also known as manual typecasting.
#Explicit typecasting is done when:
#A larger data type is converted to a smaller data type.
#For example, converting a float to an integer.
#Typecasting functions in python
#There are several typecasting functions in python that are used to convert one data type to another data type.
#The following are the typecasting functions in python:
#int(): This function is used to convert a data type to an integer data type.
#float(): This function is used to convert a data type to a float data type.
#str(): This function is used to convert a data type to a string data type.
#bool(): This function is used to convert a data type to a boolean data type.
#list(): This function is used to convert a data type to a list data type.
#tuple(): This function is used to convert a data type to a tuple data type.
#set(): This function is used to convert a data type to a set data type.
#dict(): This function is used to convert a data type to a dictionary data type.
#complex(): This function is used to convert a data type to a complex data type.
